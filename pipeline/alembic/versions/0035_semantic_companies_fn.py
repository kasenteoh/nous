# The web's semantic-search read path. SQL-language + STABLE so Postgres can
# inline/plan it per call; casts to text because the underlying columns are
# varchar and SQL-language functions require exact return-type matches.
_SEMANTIC_COMPANIES_FN = """
CREATE FUNCTION semantic_companies(query_embedding vector(384), match_count integer DEFAULT 30)
RETURNS TABLE (
    slug text,
    name text,
    hq_city text,
    hq_state text,
    industry_group text,
    description_short text,
    status text,
    logo_url text,
    similarity double precision
)
LANGUAGE sql
STABLE
PARALLEL SAFE
AS $$
    SELECT
        c.slug::text,
        c.name::text,
        c.hq_city::text,
        c.hq_state::text,
        c.industry_group::text,
        c.description_short::text,
        c.status::text,
        c.logo_url::text,
        1 - (c.embedding <=> query_embedding) AS similarity
    FROM companies c
    WHERE c.embedding IS NOT NULL
      AND c.exclusion_reason IS NULL
      AND (c.description_short IS NOT NULL OR c.funding_round_count > 0)
    ORDER BY c.embedding <=> query_embedding
    LIMIT match_count
$$;
"""


def upgrade() -> None:
    op.execute(_SEMANTIC_COMPANIES_FN)


